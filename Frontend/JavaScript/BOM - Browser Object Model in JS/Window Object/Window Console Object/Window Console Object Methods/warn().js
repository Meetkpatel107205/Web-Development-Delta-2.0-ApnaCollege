
// ---: Window Console Object Method : console.warn() :---

console.warn("This is a warning!");

/*

     ---> console.warn() OR window.console.warn() : The warn() method writes a warning to the console.

*    ---> Syntax : console.warn(message); ---OR---
*                  window.console.warn(message);

     --->   +===========+===================================================+
            | Parameter | Description                                       |
            +===========+===================================================+
            | message   | -> Required.                                      |
            |           | -> The message (warning) to write to the console. |
            +===========+===================================================+
   
*/