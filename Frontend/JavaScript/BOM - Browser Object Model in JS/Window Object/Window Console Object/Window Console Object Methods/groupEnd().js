
// ---: Window Console Object Method : console.groupEnd() :---

console.log("Hello World!");
console.group();
console.log("Hello again, this time inside a group!");
console.groupEnd();
console.log("and we are back.");

/*

      ---> console.groupEnd() OR window.console.groupEnd() : The groupEnd() ends a message group.
 
*     ---> Syntax : console.groupEnd(); ---OR---
*                   window.console.groupEnd();
 
*/