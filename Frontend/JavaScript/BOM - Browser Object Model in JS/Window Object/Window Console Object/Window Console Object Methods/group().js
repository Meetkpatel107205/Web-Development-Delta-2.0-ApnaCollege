
// ---: Window Console Object Methods : console.group() :---

console.log("Hello World!");
console.group();
console.log("Hello again, this time inside a group!");
console.log("My name is Meet K. Patel");

/*

      ---> console.group() OR window.console.group() : The group() method starts a message group.
           All new messages will be written inside this group.
 
*     ---> Syntax : console.group(Label); ---OR---
*                   window.console.group(Label);
 
      ---> +============+==========================+
           | Parameter  | Description              |
           +============+==========================+
           | label      | -> Optional.             |
           |            | -> A label for the group |
           +============+==========================+

*/