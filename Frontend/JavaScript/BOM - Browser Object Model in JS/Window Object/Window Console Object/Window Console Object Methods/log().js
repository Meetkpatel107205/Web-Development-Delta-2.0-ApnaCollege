
// ---: Window Console Object Method : console.log() :---

console.log("Hello World!");

/*

     ---> console.log() OR window.console.log() : The log() method writes (logs) a message to the console.
          The log() method is useful for testing purposes.

*    ---> Syntax : console.log(message); ---OR---
*                  window.console.log();

     ---> +===========+=========================================+
          | Parameter | Description                             |
          +===========+=========================================+
          | message   | -> Required.                            |
          |           | -> The message to write to the console. |
          +===========+=========================================+

*/