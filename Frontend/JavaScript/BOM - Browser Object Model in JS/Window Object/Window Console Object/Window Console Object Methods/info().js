
// ---: Window Console Object Method : console.info() :---

console.info("Hello World!");

/*

     ---> console.info() OR window.console.info() : The info() method writes a message to the console.

*    ---> Syntax : console.info(message); ---OR---
*                  window.console.info(message);

     ---> +===========+=========================================+
          | Parameter | Description                             |
          +===========+=========================================+
          | message   | -> Required.                            |
          |           | -> The message to write to the console. |
          +===========+=========================================+

*/