/*

      ---> console.clear() OR window.console.clear() : The clear() method clears the console.The clear() method also
           write "Console was cleared" in the console.
      
*     ---> Syntax : console.clear(); ---OR---
*                   window.console.clear();
 
      ---> +============+===============================================+
           | Parameter  | Description                                   |
           +============+===============================================+
           | message    | -> Required.                                  |
           |            | -> The error message to write in the console. |
           +============+===============================================+

*/