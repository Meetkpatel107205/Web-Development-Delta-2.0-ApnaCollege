
// ---: indexOf(arg) :---

let msg = "ILoveCoding";

console.log("Message :",msg);

console.log("First Index of Occurrence of \"Love\" :",msg.indexOf("Love"));
console.log("First Index of Occurrence of \"o\" :",msg.indexOf("o"));
console.log("First Index of Occurrence of \"z\" :",msg.indexOf("z"));

/* ---> indexOf() :- Returns the first index of occurrence of some value in String.
                     Or gives -1 if not found. */