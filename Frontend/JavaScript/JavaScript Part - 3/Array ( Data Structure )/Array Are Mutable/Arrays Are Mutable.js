
// ---: Arrays Are Mutable :---

let fruits = ["Mango","Apple","Litchi"];

console.log(" ")
console.log("---: Printing \"fruits\" Array without any Modification :---");
console.log(" ");

console.log(fruits);

fruits[0] = "Banana";
fruits[1] = "Pineapple";
fruits[10] = "Papaya";

console.log(" ");
console.log("---: Printing \"fruits\" Array with Modification :---");
console.log(" ");

console.log(fruits);

/* ---> Arrays are mutable in JavaScript while strings are immutable. */

