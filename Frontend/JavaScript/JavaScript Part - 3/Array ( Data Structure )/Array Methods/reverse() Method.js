
// ---: reverse() Method :---

let cars = ["Audi","BMW","XUV","Maruti"];

console.log(" ")
console.log("---: Printing \"cars\" Array before performing reverse() Method :---");
console.log(" ");

console.log(cars);

cars.reverse();

console.log(" ")
console.log("---: Printing \"cars\" Array after performing reverse() Method :---");
console.log(" ");

console.log(cars);

