
// ---: Shift Method :---

let cars = ["Audi","BMW","XUV","Maruti","Toyota","Ferrari"];

console.log(" ");
console.log("---: Printing \"cars\" Array before applying shift Method :---");
console.log(" ");

console.log(cars);

console.log(" ");

console.log("Element that shifted out form \"cars\" Array :",cars.shift());

console.log(" ");
console.log("---: Printing \"cars\" Array after applying shift Method :---");
console.log(" ");

console.log(cars);

