
// ---: String Operators : Lexicographical Comparison Operators :---

let s1 = "age";
let s2 = "bat";

console.log("s1 =",s1);
console.log("s2 =",s2);

console.log("\ns1 < s2 :",s1 < s2); // ---> Result = true
console.log("s1 > s2 :",s1 > s2); // ---> Result = false
console.log("s1 <= s2 :",s1 <= s2); // ---> Result = true
console.log("s1 >= s2 :",s1 >= s2); // ---> Result = false

/*

      ---> Lexicographical Comparison Operators(<,>,<=,>=) : Compares strings based on Unicode values.

*/