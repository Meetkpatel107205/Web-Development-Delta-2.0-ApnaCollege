
// ---: String Operators : Concatenate Assignment Operator :---

let s1 = "Geeks";
let s2 = "forGeeks";

console.log("s1 =",s1);
console.log("s2 =",s2);

s1 += s2;

console.log("\n---: Printing \"s1\" After Performing Operation \"s1 += s2\" :---")

console.log("\ns1 =",s1);

/*

     ---> Concatenate Assignment Operator : We can modify one string by adding content of another to it.

*/