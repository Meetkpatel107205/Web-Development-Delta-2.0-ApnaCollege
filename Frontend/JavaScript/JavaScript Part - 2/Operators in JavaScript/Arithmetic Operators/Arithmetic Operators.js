
// ---: Arithmetic Operators :---

let a = 10;
let b = 5;

console.log("a =",a);
console.log("b =",b);
console.log("\na + b =",(a + b)); // ---> Result = 15  
console.log("a - b =",(a - b)); // ---> Result = 5
console.log("a * b =",(a * b)); // ---> Result = 50
console.log("a / b =",(a / b)); // ---> Result = 2
console.log("a % b =",(a % b)); // ---> Result = 0
console.log("a ^ b =",(a ** b)); // ---> Result = 100000

