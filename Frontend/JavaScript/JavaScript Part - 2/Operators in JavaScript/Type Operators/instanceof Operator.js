
// ---: instanceof Operator :---

class Animal {}
let dog = new Animal();

console.log("dog instanceof Animal :",dog instanceof Animal); // ---> Result = true
console.log("[] instanceof Array :",[] instanceof Array);   // ---> Result = true
console.log("{} instanceof Object :",{} instanceof Object);  // ---> Result = true

