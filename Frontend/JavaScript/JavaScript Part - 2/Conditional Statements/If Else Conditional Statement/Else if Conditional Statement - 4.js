
// ---: Else if Conditional Statement - 4 :---

let color = "Yellow";

if(color === "Red")
{
    console.log("Stop! Light color is Red");
}
else if(color === "Yellow")
{
    console.log("Slow Down. Light color is Yellow");
}
else if(color === "Green")
{
    console.log("Go. Light color is Green");
}