
// ---: Nesting If else :---

let Marks = 45;

if(Marks >= 33)
{
    console.log("Pass");

    if(Marks >= 80)
    {
        console.log("Grade : 0")
    }
    else
    {
        console.log("Grade : A")
    }
}
else
{
    console.log("Better Luck next time!");
}

/* 

     ---> Nesting :- Nesting is writing if-else inside if-else statements.It can have 
                     many levels.
                     
*/